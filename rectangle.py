def area(a, b):
    '''на вход получает 'a' первую сторону прямоугольника и 'b' вторую сторону прямоугольника
    возвращает по формуле площади a*b'''
    return a * b

def perimeter(a, b):
    '''на вход получает 'a' первую сторону прямоугольника и 'b' вторую сторону прямоугольника
    возвращает по формуле периметра 2*(a+b)'''
    return 2 * (a + b)