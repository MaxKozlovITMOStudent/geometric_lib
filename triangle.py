def area(a, h):
    '''на вход получает сторону треугольника 'a' и высоту 'h' проведенную к ней
    по формуле площади a*h/2 выводит площадь треугольника'''
    return a * h / 2 

def perimeter(a, b, c):
    '''на вход получает 3 стороны треугольника a,b,c
    по формуле периметра a+b+c выводит периметр треугольника'''
    return a + b + c
